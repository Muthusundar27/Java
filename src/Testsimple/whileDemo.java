package Testsimple;

public class whileDemo {

	public static void main(String[] args) {
		
		// Incremental
		int i = 0;
        while(i<10) {
        	System.out.println(i);
        	i++;
        }
        System.out.println("********");
        // decremental
        int j = 10;
        while(j>0) {
        	System.out.println(j); 
        	j--;
        }
	}

}
